package com.ea.exam.entity;

import jakarta.persistence.Entity;

@Entity
public class Person extends Owner {
    private int age;

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    @Override
    public String toString() {
        return "Person{" +
                "age=" + age +
                '}';
    }
}
